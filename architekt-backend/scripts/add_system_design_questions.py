def main():
    db = SessionLocal()
    try:
        cats = db.execute(select(Category)).scalars().all()
        cat_map = {c.slug: c.id for c in cats}

        missing = {q["category_slug"] for q in NEW_QUESTIONS} - set(cat_map)
        if missing:
            print(f"WARNING: missing categories: {missing}")

        inserted = 0
        skipped = 0
        for q in NEW_QUESTIONS:
            if q["category_slug"] not in cat_map:
                skipped += 1
                continue
            existing = db.execute(
                select(Question).where(Question.question_text == q["question_text"])
            ).scalar_one_or_none()
            if existing:
                skipped += 1
                continue
            db.add(Question(
                id=uuid.uuid4(),
                category_id=cat_map[q["category_slug"]],
                subcategory=q["subcategory"],
                tags=q.get("tags", []),
                difficulty=q["difficulty"],
                question_type=getattr(q["question_type"], "value", q["question_type"]),
                question_text=q["question_text"],
                options=q["options"],
                correct_answer=q["correct_answer"],
                explanation=q["explanation"],
                hint=q.get("hint", ""),
                related_concepts=[],
                source=getattr(QuestionSource.MANUAL, "value", QuestionSource.MANUAL),
                review_status=getattr(ReviewStatus.APPROVED, "value", ReviewStatus.APPROVED),
                times_answered=0,
                times_correct=0,
                created_at=datetime.utcnow(),
                updated_at=datetime.utcnow(),
            ))
            inserted += 1

        db.commit()
        print(f"Inserted {inserted} new questions. Skipped {skipped} (duplicates or missing category).")
    except Exception as e:
        db.rollback()
        print(f"Error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()
